#include "3-calc.h"
/**
 * op_add - add 2 integers
 * @a: integer one
 * @b: integer two
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract 2 integers
 * @a: integer one
 * @b: integer two
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 integers
 * @a: integer one
 * @b: integer two
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 integers
 * @a: integer one
 * @b: integer two
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divide 2 integers and take the rest of the division
 * @a: integer one
 * @b: integer two
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
